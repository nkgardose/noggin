export enum KeyboardEvents {
  R = 'r', // roll
  ArrowUp = 'ArrowUp', // go up player
  ArrowDown = 'ArrowDown', // go down player
  ArrowRight = 'ArrowRight', // increment point for a player
  ArrowLeft = 'ArrowLeft', // decrement point for a player
  Backspace = 'Backspace', // delete a player
  Enter = 'Enter', // add a player
}
